
############### Supression In Regression ##################

# Suppressions can be defined as “a variable which increases the predictive validity 
# of another variable (or set of variables) by its inclusion in a regression equation,” 
# a suppression effect would be present when the direct and indirect effects of an independent 
# variable on a dependent variable have opposite signs.


# the expectation from adding any new variable to the regression equation is to get higher R, 
# but if R decrease, then this is called suppression.